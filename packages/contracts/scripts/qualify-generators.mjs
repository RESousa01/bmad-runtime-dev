import path from "node:path";
import { readFile } from "node:fs/promises";
import Ajv2020 from "ajv/dist/2020.js";
import { compile as generateTypescript } from "json-schema-to-typescript";
import {
  buildInternalBundle,
  generateNativeTrees,
  loadAndValidateToolLock,
  repositoryRoot,
  sha256,
  synchronizeTree,
  treeDigest,
  treeRecords,
} from "./lib/native-codegen.mjs";
import { parseStrictJson } from "./lib/strict-json.mjs";

const argumentsList = process.argv.slice(2);
const checkOnly = argumentsList.includes("--check");
const writeMode = argumentsList.includes("--write");
const dryRun = argumentsList.includes("--dry-run");
const unknown = argumentsList.filter((argument) =>
  argument !== "--check" && argument !== "--write" && argument !== "--dry-run");
if (unknown.length > 0 || [checkOnly, writeMode, dryRun].filter(Boolean).length !== 1) {
  throw new Error("Use exactly one of --check, --write, or --dry-run with qualify-generators.mjs.");
}

const lock = await loadAndValidateToolLock();
const qualificationBundle = await buildInternalBundle(lock, "qualification");
const qualificationRoot = path.join(repositoryRoot, "tests", "generator-qualification");
const catalog = parseStrictJson(await readFile(path.join(qualificationRoot, "catalog.json"), "utf8"));
const originalRoot = parseStrictJson(
  await readFile(path.join(qualificationRoot, catalog.rootSchema), "utf8"),
);
const originalAjv = new Ajv2020({ allErrors: true, strict: true, validateFormats: false });
for (const resourcePath of catalog.resources) {
  originalAjv.addSchema(parseStrictJson(
    await readFile(path.join(qualificationRoot, resourcePath), "utf8"),
  ));
}
const originalValidator = originalAjv.compile(originalRoot);
const bundleValidator = new Ajv2020({ allErrors: true, strict: true, validateFormats: false })
  .compile(qualificationBundle.bundle);
for (const fixture of catalog.fixtures) {
  const source = await readFile(path.join(qualificationRoot, fixture.file), "utf8");
  let value;
  try {
    value = parseStrictJson(source, catalog.parserLimits);
  } catch (error) {
    if (fixture.rejectionStage !== "strict_parser") throw error;
    continue;
  }
  const originalAccepted = originalValidator(value);
  const bundleAccepted = bundleValidator(value);
  if (originalAccepted !== bundleAccepted) {
    throw new Error(
      `CONTRACT_LANGUAGE_PARITY_FAILED: presence transform changed fixture ${fixture.id}.`,
    );
  }
  if (originalAccepted !== (fixture.expected === "accept")) {
    throw new Error(
      `CONTRACT_LANGUAGE_PARITY_FAILED: fixture ${fixture.id} contradicts its catalog.`,
    );
  }
}
const typescriptOptions = {
  bannerComment:
    "// @generated by json-schema-to-typescript 15.0.4 via scripts/qualify-generators.mjs; DO NOT EDIT.",
  format: true,
  ignoreMinAndMaxItems: true,
  unreachableDefinitions: false,
};
const generateQualificationTypescript = async () => {
  const generated = await generateTypescript(
    qualificationBundle.bundle,
    lock.sourceSet.qualification.rootType,
    typescriptOptions,
  );
  return generated.replaceAll("\r\n", "\n").replaceAll("\r", "\n").replace(/\n*$/u, "\n");
};
const [typescriptRunA, typescriptRunB] = await Promise.all([
  generateQualificationTypescript(),
  generateQualificationTypescript(),
]);
if (typescriptRunA !== typescriptRunB) {
  throw new Error(
    "CONTRACT_GENERATOR_NONDETERMINISTIC: qualification TypeScript output changed across clean runs.",
  );
}

const native = await generateNativeTrees("qualification");
const expected = new Map([["typescript/qualification.ts", typescriptRunA]]);
for (const [relativePath, source] of native.rust) {
  expected.set(`rust/${relativePath}`, source);
}
for (const [relativePath, source] of native.dotnet) {
  expected.set(`dotnet/${relativePath}`, source);
}
const generatedRoot = path.join(
  repositoryRoot,
  "tests",
  "generator-qualification",
  "generated",
);
if (!dryRun) await synchronizeTree(generatedRoot, expected, checkOnly);
const records = treeRecords(expected, "tests/generator-qualification/generated");
const action = dryRun ? "dry-ran" : checkOnly ? "checked" : "wrote";
console.log(
  `contracts: qualification generators ${action} ${records.length} files `
    + `(${treeDigest(records)}, bundle ${sha256(qualificationBundle.source)})`,
);
