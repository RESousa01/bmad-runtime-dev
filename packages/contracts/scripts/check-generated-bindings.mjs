import assert from "node:assert/strict";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { loadBindingCheckInputs } from "./lib/controlled-contract-io.mjs";
import { parseStrictJson } from "./lib/strict-json.mjs";

const packageRoot = fileURLToPath(new URL("../", import.meta.url));
const repositoryRoot = path.resolve(packageRoot, "..", "..");
const {
  dotnetFiles,
  dotnetFileSources,
  lock,
  lockedGeneratedSources,
  rustSource,
  schemaSources,
  toolLock,
  typescriptSchemaSources,
  typescriptSource,
} = await loadBindingCheckInputs({ packageRoot, repositoryRoot });
assert.ok(dotnetFiles.length > 0, "Corvus generated C# tree is empty.");
for (const [index, source] of dotnetFileSources.entries()) {
  assert.match(source, /<auto-generated>/u, `${dotnetFiles[index]} lacks Corvus's generated marker.`);
}
const sources = {
  dotnet: dotnetFileSources.join("\n"),
  rust: rustSource,
  typescript: typescriptSource,
};
sources.typescript = [
  sources.typescript,
  ...typescriptSchemaSources,
].join("\n");
const expectedFamilies = toolLock.sourceSet.production.roots.map(({ file }) =>
  file.replace(/\.schema\.json$/u, ""));
const expectedRootTypes = toolLock.sourceSet.production.roots.map(({ typeName }) => typeName);
for (const language of ["typescript", "rust", "dotnet"]) {
  assert.equal(lock.languageCoverage[language].status, "generated", `${language} is not generated.`);
  assert.deepEqual(
    lock.languageCoverage[language].families,
    expectedFamilies,
    `${language} family coverage differs from the closed source set.`,
  );
  assert.deepEqual(lock.languageCoverage[language].dependencies, ["common"]);
}
const deferredRunnerVocabulary = [
  "WindowsContainmentClaim",
  "WindowsLocalHostAudience",
  "runnerProfile",
  "runner_profile",
  "RunnerProfile",
  "job_object_controlled",
  "childProcess",
  "child_process",
  "ChildProcess",
  "networkIntent",
  "network_intent",
  "NetworkIntent",
  "standard_user_job",
  "windows_local_host",
  "command_run",
  "raw_shell",
  "run_shell",
  "process_spawn",
];
const deferredBmadVocabulary = [
  "SkillPackageCandidate",
  "SkillPackageVersion",
  "PackageRegistration",
  "PackagePublication",
  "InstallRehearsalRun",
  "InvocationRehearsalRun",
  "EvaluationRun",
  "PackagePromotionRequest",
  "PackageActivation",
  "PackageRollback",
  "BuilderModule",
  "BuilderRegistration",
  "BuilderRehearsal",
  "BuilderEvaluation",
  "BuilderPublication",
  "BuilderPromotion",
  "BuilderActivation",
  "BuilderRollback",
  "BuilderMemoryAgent",
  "BuilderAutonomousAgent",
  "RegisterBuilder",
  "RehearseBuilder",
  "EvaluateBuilder",
  "PublishBuilder",
  "PromoteBuilder",
  "ActivateBuilder",
  "RollbackBuilder",
];

for (const [runtime, source] of Object.entries(sources)) {
  if (runtime === "typescript") {
    assert.match(source, /generated/iu, "TypeScript binding must carry a generated marker.");
  }
  for (const symbol of expectedRootTypes) {
    assert.ok(source.includes(symbol), `${runtime} binding is missing ${symbol}.`);
  }
  assert.ok(!source.includes("CommandCandidate"), `${runtime} must not expose D4 commands.`);
  assert.ok(
    source.includes("NativePatchEngineAudience"),
    `${runtime} binding must expose the D3 native patch-engine audience.`,
  );
  for (const forbidden of deferredRunnerVocabulary) {
    assert.ok(
      !source.includes(forbidden),
      `${runtime} binding must not expose deferred runner vocabulary: ${forbidden}.`,
    );
  }
  for (const forbidden of deferredBmadVocabulary) {
    assert.ok(
      !source.includes(forbidden),
      `${runtime} binding must not expose deferred BMAD vocabulary: ${forbidden}.`,
    );
  }
}

for (const [schemaName, source] of schemaSources) {
  for (const forbidden of deferredRunnerVocabulary) {
    assert.ok(
      !source.includes(forbidden),
      `${schemaName} must not expose deferred runner vocabulary: ${forbidden}.`,
    );
  }
  for (const forbidden of deferredBmadVocabulary) {
    assert.ok(
      !source.includes(forbidden),
      `${schemaName} must not expose deferred BMAD vocabulary: ${forbidden}.`,
    );
  }
}

assert.ok(!/\bany\b/.test(sources.typescript), "Generated TypeScript must not use any.");
assert.ok(sources.rust.includes("deny_unknown_fields"));
assert.ok(sources.dotnet.includes("IJsonElement"));
assert.ok(sources.dotnet.includes("JsonSchemaContext"));

for (const record of lock.generatedTree.files) {
  const source = lockedGeneratedSources.get(record.file);
  const actual = `sha256:${createHash("sha256").update(source, "utf8").digest("hex")}`;
  assert.equal(actual, record.sha256, `${record.file} differs from schema-lock.json.`);
}

function toSnakeCase(value) {
  return value.replace(/[A-Z]/g, (letter) => `_${letter.toLowerCase()}`);
}

function toPascalCase(value) {
  return `${value[0].toUpperCase()}${value.slice(1)}`;
}

for (const schemaName of [
  "candidate-action.schema.json",
  "approved-execution-spec.schema.json",
  "spec-consumption.schema.json",
  "execution-result-manifest.schema.json",
  "evidence-event.schema.json",
]) {
  const schema = parseStrictJson(schemaSources.get(schemaName));
  for (const propertyName of schema.required) {
    assert.ok(
      sources.typescript.includes(`${propertyName}:`),
      `TypeScript binding omits required ${schemaName} property ${propertyName}.`,
    );
    assert.ok(
      sources.rust.includes(`pub ${toSnakeCase(propertyName)}:`),
      `Rust binding omits required ${schemaName} property ${propertyName}.`,
    );
    assert.ok(
      sources.dotnet.includes(toPascalCase(propertyName)),
      `C# binding omits required ${schemaName} property ${propertyName}.`,
    );
  }
}

console.log(
  "contracts: baseline cross-language and extended TypeScript binding checks passed",
);
